let murderer = "Professor Plum";
suspects = ["Mrs. Peacock", "Miss Scarlet", "Colonel Mustard"];

function whoDunnit(){
  let murderer;
  for(let suspect of suspects){
    murderer = suspect;
  }
  return murderer
}

murderer = whoDunnit()
console.log(`The murderer is ${murderer}`);
